// Função para filtrar documentos por categoria
export const getDocumentsByCategory = (category) => {
  const categoryData = theologicalDocuments.find(cat => cat.category === category);
  return categoryData ? categoryData.documents : [];
};

// Função para filtrar documentos por dificuldade
export const getDocumentsByDifficulty = (difficulty) => {
  const allDocuments = theologicalDocuments.flatMap(cat => cat.documents);
  return allDocuments.filter(doc => doc.difficulty.includes(difficulty));
};

// Função para buscar documentos por tópico
export const getDocumentsByTopic = (topic) => {
  const allDocuments = theologicalDocuments.flatMap(cat => cat.documents);
  return allDocuments.filter(doc => 
    doc.topics.some(t => t.toLowerCase().includes(topic.toLowerCase()))
  );
};

